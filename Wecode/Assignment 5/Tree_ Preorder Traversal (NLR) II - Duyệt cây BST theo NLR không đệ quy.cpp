    void preOrder(Node *root) {

        if (root == NULL) return;

        stack<Node*> s;
        s.push(root);
        
        while (!s.empty()) {
            Node* current = s.top();
            s.pop();
            cout << current->data << " "; // Process the current node

            // Push the right child first (since we want to process left subtree first)
            if (current->right) 
                s.push(current->right);
            // Push the left child (to be processed next)
            if (current->left) 
                s.push(current->left);
        
    }

}; //End of Solution

